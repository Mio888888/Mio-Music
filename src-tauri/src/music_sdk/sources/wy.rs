fn aes_cbc_encrypt(data: &[u8], key: &[u8; 16], iv: &[u8; 16]) -> Vec<u8> {
    let mut buf = vec![0u8; data.len() + 16]; // extra space for padding
    buf[..data.len()].copy_from_slice(data);
    let encryptor: CbcEncryptor<Aes128> = CbcEncryptor::new(key.into(), iv.into());
    encryptor.encrypt_padded_mut::<Pkcs7>(&mut buf, data.len()).unwrap().to_vec()
}

fn aes_ecb_encrypt(data: &[u8], key: &[u8; 16]) -> Vec<u8> {
    let cipher = Aes128::new(key.into());
    let mut buf = vec![0u8; data.len() + 16];
    buf[..data.len()].copy_from_slice(data);
    cipher.encrypt_padded_mut::<Pkcs7>(&mut buf, data.len()).unwrap().to_vec()
}

